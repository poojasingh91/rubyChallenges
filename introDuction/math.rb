#You can embed Ruby code inside the hashtag and bracket (#{}),you can only do this when using double quotes (""). This will not work with single quotes ('').
#You can calculate the remainder of two numbers that are divided by each other with the modulo % operator

puts "2+3 is #{2+3}"
puts "0.4 * 10 is #{0.4*10}"
puts "10/3 is #{10/3}"
puts "10.0/3 is #{10.0/3}"
puts "7%3 is #{7%3}"
